TAGS = ["diffae_model_training"]


def main(
    resolution_level: int = 1,
    crop_size: int = 128,
    exclude_cell_piling: bool = False,
) -> None:
    """
    Train a DiffAE model using the provided configuration.

    **Cell piling exclusion**

    By default, timepoints marked as having cell piling annotations are included in the training
    and validation datasets created by ``create-diffae-training-dataframe``, unless the input
    parameter ``exclude_cell_piling`` is set to True. This means that by default, the model
    will be trained on data that includes cell piling. To train a model that does not "see"
    cell piling, first run the ``create-diffae-training-dataframe`` script with the flag
    ``--exclude-cell-piling``, and then run this training script with the same flag.

    When ``exclude_cell_piling`` is False, the workflow will use the "standard" DataframeManifest
    object named ``diffae_training_dataframe_resolution_{resolution_level}`` for training.
    When True, the workflow will find the DataframeManifest object with the suffix
    ``_exclude_cell_piling`` and use the corresponding training and validation datasets.

    **Workflow demo**

    If demo mode is enabled, the training and validation datasets used will be
    the ones with only one entry each as generated by the
    ``create_diffae_training_dataframe`` script with the ``--demo-mode`` (or
    ``-d``) flag. Furthermore, the model will only be trained for one epoch.

    Parameters
    ----------
    resolution_level
        The resolution level of the zarr files to be used for training.
    crop_size
        The length of the 2D image crop in pixels to use for model training.
    exclude_cell_piling
        If True, use training and validation datasets that exclude cell piling timepoints.

    Returns
    -------
    :
        The function creates and saves a ModelConfig object with the trained
        model's MLflow run ID and the list of datasets used for training.
    """

    import logging
    from pathlib import Path

    from omegaconf import OmegaConf

    from endo_pipeline import DEMO_MODE
    from endo_pipeline.io import get_output_path, make_name_unique, resolve_dataframe_location
    from endo_pipeline.library.model import (
        get_dataset_names_used_for_training,
        get_model_dir,
        initialize_diffae_model,
    )
    from endo_pipeline.manifests import (
        ModelLocation,
        ModelManifest,
        load_dataframe_manifest,
        load_model_manifest,
        save_model_manifest,
    )

    logger = logging.getLogger(__name__)

    # Adjust workflow settings for demo mode. Append a suffix to the manifest
    # and model names to indicate that they were generated from a workflow demo,
    # and reduce the number of epochs and logging steps. Also adjust the cache rate.
    if DEMO_MODE:
        name_suffix = "_test_workflow"
        max_num_epochs = 1
        log_every_n_steps = 1
        cache_rate = 1.0  # use 100% of data for demo mode
        replace_rate = 0.1
    else:
        name_suffix = ""
        max_num_epochs = 1000
        log_every_n_steps = 50
        cache_rate = 1.0  # This can be changed if needed!
        replace_rate = 0.5

    # "_exclude_cell_piling" suffix if cell piling exclusion is requested
    if exclude_cell_piling:
        name_suffix = f"_exclude_cell_piling{name_suffix}"

    manifest_name = f"diffae_training_dataframe_resolution_{resolution_level}{name_suffix}"

    try:
        dataframe_manifest = load_dataframe_manifest(manifest_name)
    except FileNotFoundError:
        logger.error(
            "Dataframe manifest [ %s ] for resolution_level [ %s ] not found. "
            "Please run the create_diffae_training_dataframe script first "
            "with the appropriate resolution_level.",
            manifest_name,
            resolution_level,
        )
        raise

    train_dataframe_location = dataframe_manifest.locations["training"]
    val_dataframe_location = dataframe_manifest.locations["validation"]

    # get csv paths from the DataframeLocation objects
    # to pass into the DiffAE model training script
    # (need csv paths for training config setup and CytoDL dataloaders)
    train_dataframe_path = resolve_dataframe_location(train_dataframe_location)
    val_dataframe_path = resolve_dataframe_location(val_dataframe_location)

    # load template training config
    template_training_config = OmegaConf.load(get_model_dir() / "diffae_training.yaml")

    # set model name via zarr resolution, crop size, and current timestamp
    model_name = f"diffae_resolution_{resolution_level}_patch_{crop_size}x{crop_size}"

    # add info about cell piling inclusion/exclusion
    if exclude_cell_piling:
        model_name = f"{model_name}_exclude_cell_piling"
    else:
        model_name = f"{model_name}_include_cell_piling"

    # append timestamp to get unique model name
    # TODO: we can be more precise about how model names and run names are
    # specified, including allowing the user to pass a run name as an argument
    model_name_unique = make_name_unique(model_name).name
    run_name = model_name_unique.replace(f"{model_name}_", "")
    logger.info("Model name: [ %s ]", model_name)
    logger.info("Run name: [ %s ]", run_name)

    # initialize DiffAE model: generates config overrides and sets up output directories
    model = initialize_diffae_model(
        template_training_config,
        crop_size,
        model_name_unique,
        train_dataframe_path,
        val_dataframe_path,
        max_num_epochs=max_num_epochs,
        log_every_n_steps=log_every_n_steps,
        cache_rate=cache_rate,
        replace_rate=replace_rate,
    )
    local_config_save_path = get_output_path("models", "training_configs")
    model.save_config(local_config_save_path / f"{model_name_unique}_train.yaml")
    logger.info(
        "Training config saved to [ %s ]",
        local_config_save_path / f"{model_name_unique}_train.yaml",
    )
    _, object_dict = model.train()

    # retrive MLflow run ID
    mlflow_logger = object_dict["logger"][0]
    run_id = mlflow_logger.run_id
    logger.info("MLflow run ID: [ %s ]", run_id)

    # get list of datasets used for training
    # based on content of train and val dataframes
    list_of_training_datasets = get_dataset_names_used_for_training(
        train_dataframe_location,
        val_dataframe_location,
        "live_20X_objective_3i_microscope",
    )

    # Create a new model manifest with workflow parameters, if a matching
    # manifest does not already exist. Add the model training run to the list
    # of manifest locations.
    try:
        manifest = load_model_manifest(model_name)
    except FileNotFoundError:
        parameters = {
            "training_datasets": list_of_training_datasets,
            "crop_size": crop_size,
            "resolution_level": resolution_level,
            "exclude_cell_piling": exclude_cell_piling,
        }
        manifest = ModelManifest(
            name=model_name, parameters=parameters, workflow=Path(__file__).stem
        )

    manifest.locations[run_name] = ModelLocation(mlflow_run_id=run_id)
    save_model_manifest(manifest)


if __name__ == "__main__":
    from endo_pipeline.__main__ import workflow_cli

    workflow_cli(main)
