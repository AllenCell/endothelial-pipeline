def ddd_model_analysis(
    name: str,
    model: list[Callable],
    stationary_data: pd.DataFrame,
    shear: float,
    pcs: list[int],
    bins: list[np.ndarray],
    fig_savedir: str,
    config: dict,
) -> None:
    """
    Generate and save figures analyzing the data-driven dynamics model.

    Inputs:
    - name (str): name of dataset
    - model (list[Callable]): list of callable functions
        representing the drift and diffusion estimates
        for the data-driven dynamics model
    - stationary_data: DataFrame, feature data for one dataset
        at one flow condition within that dataset, restricted to
        only the frames where the data are stationary
    - shear (float): shear stress value for the dataset
    - pcs (list[int]): list of principal components to analyze
        e.g., if pcs = [0,1], analyzing data projected onto
        the first two principal components
    - bins (list[np.ndarray]): list of bin edges for the histogram
        plots
    - fig_savedir (str): directory to save figures
    - config (dict): configuration dictionary
        Loaded from dynamics_config.yaml
    - stationary_frames: list of int, optional, if provided,
    restrict data to only these frames
    (e.g., last 100 frames of each flow condition)
    as our cutoff for stationary data
        - if None, return all data
        - this may be hardcoded in the data config in the future


    Outputs:
    - None: saves figures to fig_savedir
        Figures include phase plane plots and histograms
        comparing model prediction (stationary FPE solution
        using drift and diffusion estimates) to data
    """
    # for phase plane plots, grid is fixed and
    # set in the config file dynamics_config.yaml
    pplane_xlim = config["plt_xlim"]["pplane"]
    pplane_ylim = config["plt_ylim"]["pplane"]
    num_pts_pplane = config["num_pts_pplane"]

    pplane_xvec = np.linspace(pplane_xlim[0], pplane_xlim[1], num_pts_pplane + 1)
    pplane_yvec = np.linspace(pplane_ylim[0], pplane_ylim[1], num_pts_pplane + 1)

    # call main model analysis function
    fig1, _, fig2, _ = model_analysis.model_data_comparison_one_dataset(
        model,
        stationary_data,
        shear,
        pcs,
        bins,
        pplane_xvec,
        pplane_yvec,
    )

    # add title to histogram plots
    sup_title = fig2.texts[0].get_text()
    sup_title = name + f", {shear} dyn/cm$^2$ \n" + sup_title
    fig2.suptitle(sup_title, fontsize=fig2.texts[0].get_fontsize(), y=1.15)

    # save figures
    viz_base.save_plot(fig1, fig_savedir + name + f"_ddff_phase_portrait_shear_{int(shear)}")
    viz_base.save_plot(fig2, fig_savedir + name + f"_ddff_stationary_dist_shear_{int(shear)}")
    return


def get_and_analyze_ddd(
    name: str, pca: Pipeline, kernel_params: dict | None, fig_savedir: str, config: dict
) -> None:
    """
    Get and analyze data-driven dynamics for a given dataset.

    Inputs:
    - name (str): name of dataset
    - pca (Pipeline): fit PCA object
        Used to project data into PCA space
    - kernel_params (dict): parameters for kernel method
        Used to compute drift and diffusion estimates
        Keys:
        - "kernel": kernel function to use
            See library.analyze.kramersmoyal.km_kernels
        - "bandwidth": bandwidth for kernel function
    - fig_savedir (str): directory to save figures
    - config (dict): configuration dictionary
        Loaded from dynamics_config.yaml
    - stationary_frames: list of int, optional, if provided,
        restrict data to only these frames
        (e.g., last 100 frames of each flow condition)
        as our cutoff for stationary data
        - if None, return all data
        - this may be hardcoded in the data config in the future

    Outputs:
    - None: saves figures generated by ddd_model_analysis
        to directory fig_savedir
    """
    # unpack relevant parameters from config:

    # which PCs to analyze (2D)
    pcs = config["pcs_to_analyze"]
    # time step (in minutes)
    dt = config["dt"]

    # bin limits for ddff and histogram
    # fixed across all datasets
    bin_xlim = config["plt_xlim"]["hist"]
    bin_ylim = config["plt_ylim"]["hist"]
    num_bins_hist = config["num_bins_hist"]
    # get bins edges and centers
    bins, centers = regression_helper.get_bins(num_bins_hist, bin_limits=[bin_xlim, bin_ylim])

    # load the data for the given name
    # and preprocess it
    df_proj = preprocessing.get_manifest_for_dynamics_workflows(name, pca=pca)

    # just get PCs of interest
    feat_cols_all = manifest_io_temp.get_feature_cols(df_proj)
    feat_cols = [feat_cols_all[i] for i in pcs]

    # split out data by flow condition
    df_by_flow, shear_list = regression_helper.get_traj_by_flow(df_proj, name)
    num_flow = len(shear_list)

    # get drift and diffusion estimates
    # for each flow condition
    # and analyze the data-driven dynamics model

    for j in range(num_flow):
        # get list of per-crop trajectories and list
        # of the corresponding displacement vectors
        traj_list, d_traj_list = regression_helper.get_traj_and_diff(df_by_flow[j], feat_cols)

        # get drift and diffusion estimates (Kramers-Moyal coefficients)
        drift_km, diff_km = regression_helper.get_kramers_moyal(
            traj_list,
            d_traj_list,
            bins,
            dt,
            kernel_params=kernel_params,
        )

        # extrapolate nans in drift and diffusion estimates
        drift_dict = data_driven_flow_field.compute_extrapolated_vector_field(
            drift_km, centers, interpolator="nearest"
        )
        diffusion_dict = data_driven_flow_field.compute_extrapolated_vector_field(
            diff_km, centers, interpolator="nearest"
        )

        # turn into callable vector fields
        # have to have shear as a parameter
        # (dummy variable)
        def drift_(x: np.ndarray, u: float, vector_dict: dict = drift_dict) -> np.ndarray:
            drift = data_driven_flow_field.get_callable_vector_field(
                vector_dict, for_solve_ivp=False
            )
            return drift(x)

        def diffusion_(x: np.ndarray, u: float, vector_dict: dict = diffusion_dict) -> np.ndarray:
            diffusion = data_driven_flow_field.get_callable_vector_field(
                vector_dict, for_solve_ivp=False
            )
            return diffusion(x)

        # call main model analysis function
        # for the data driven dynamics workflow
        ddd_model_analysis(
            name,
            [drift_, diffusion_],
            df_by_flow[j],
            shear_list[j],
            pcs,
            bins,
            fig_savedir,
            config,
        )
